fn main() {
    
    let x = 5;
    println!("{}",x);

    let x = x * 5;
    println!("{}",x);

    let x : f64 = 7.548;
    println!("{}",x);

    // the above concept is called shadowing ... 
    // a type of redecleration of the same variable
    // we need to use let with variable while its redecleration
    // and also need to assign data type
}
